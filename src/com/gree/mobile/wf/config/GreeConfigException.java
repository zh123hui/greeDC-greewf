package com.gree.mobile.wf.config;

public class GreeConfigException extends RuntimeException {

	private static final long serialVersionUID = -6328656475394741404L;

	public GreeConfigException() {
		super();
	}

	public GreeConfigException(String message, Throwable cause) {
		super(message, cause);
	}

	public GreeConfigException(String message) {
		super(message);
	}

	public GreeConfigException(Throwable cause) {
		super(cause);
	}

	
}
